function cs(w: number, h: number, data: string): SpriteData {
  return {
    width: w,
    height: h,
    pixels: data.split("\n").map((line) =>
      Array.from(line).map((ch) => {
        const c = ch.charCodeAt(0);
        if (c >= 48 && c <= 57) return c - 48;
        if (c >= 65 && c <= 67) return c - 65 + 10;
        if (c >= 97 && c <= 99) return c - 97 + 10;
        return 0;
      })
    ),
  };
}

