export function getRandomMascot(): Mascot {
  return ALL_MASCOTS[Math.floor(Math.random() * ALL_MASCOTS.length)];
}

export function getMascotStorageKey(sessionId: string): string {
  return `tool_mascot_${sessionId}`;
}

export function loadMascotId(sessionId: string): string {
  const key = getMascotStorageKey(sessionId);
  let id = localStorage.getItem(key);
  if (!id || !ALL_MASCOT_MAP.has(id)) {
    id = getRandomMascot().id;
    localStorage.setItem(key, id);
  }
  return id;
}

export function saveMascotId(sessionId: string, id: string): void {
  localStorage.setItem(getMascotStorageKey(sessionId), id);
}

